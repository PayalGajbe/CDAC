package Assi4;
/*
 Write a program that demonstrates widening conversion from int to double and prints the result.
 */
public class Assi4P1 {

	public static void main(String[] args) {
		int num1 = 10;
		double num2 = num1; //As its widening we dont need to do it explicitly it happens implicitly.
		System.out.println(num2);

	}

}
